import { ScriptInput, VideoStyle } from "@/types/script";

const styleDescriptions: Record<VideoStyle, string> = {
  expert:
    "экспертный — авторитетный тон, факты, профессиональная лексика, демонстрация компетентности",
  sales:
    "продающий — акцент на выгодах, боли аудитории, призыв к действию, ограниченность предложения",
  entertainment:
    "развлекательный — юмор, неожиданные повороты, эмоции, высокая динамика",
  educational:
    "образовательный — пошаговое объяснение, простой язык, конкретные примеры, практическая польза",
};

export function buildPrompt(input: ScriptInput): string {
  return `Ты — профессиональный сценарист коротких видео для Reels, Shorts и TikTok.

Создай сценарий видео на 60 секунд по следующим параметрам:
- Тема: ${input.topic}
- Продукт/услуга: ${input.product}
- Целевая аудитория: ${input.audience}
- Стиль: ${styleDescriptions[input.style]}

Верни ответ строго в формате JSON (без markdown-обёртки, без пояснений):
{
  "title": "цепляющий заголовок видео (до 60 символов)",
  "hook": "первые 3-5 секунд видео — один короткий удар по боли или интересу, который останавливает скролл",
  "scenes": [
    {
      "number": 1,
      "duration": "0-10 сек",
      "action": "что происходит на экране",
      "frameText": "текст на экране (если есть)",
      "voiceover": "закадровый текст или речь"
    }
  ],
  "cta": "призыв к действию в конце видео (конкретный и краткий)"
}

Требования:
- Сцен должно быть 4-6 штук, суммарная длительность ~60 секунд
- Хук должен работать без звука — зрители часто листают без звука
- Каждая сцена — одна чёткая мысль или действие
- Текст на экране — максимум 5-7 слов
- CTA — конкретное действие (подписаться, написать, купить, перейти)
`;
}
